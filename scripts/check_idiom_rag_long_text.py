def _format_row(rank: int, row: Any) -> str:
    item = row.item
    return "\n".join(
        [
            f"rank: {rank}",
            f"source_id: {item.get('source_id', '')}",
            f"anchor: {item.get('anchor', '')}",
            f"matched_phrase: {item.get('matched_phrase', '')}",
            f"match_type: {item.get('match_type', '')}",
            f"confidence: {item.get('confidence', '')}",
            f"lexical_evidence: {item.get('lexical_evidence', '')}",
            f"score: {row.score:.4f}",
            f"evidence_chunk: {item.get('evidence_chunk', '')}",
        ]
    )


def _load_long_text() -> str:
    if LONG_TEXT.strip():
        return LONG_TEXT.strip()
    if not sys.stdin.isatty():
        data = sys.stdin.read().strip()
        if data:
            return data
    return ""


def main() -> int:
    long_text = _load_long_text()
    if not long_text:
        print("LONG_TEXT is empty. Paste a long Korean passage into LONG_TEXT or pipe text via stdin.")
        print("Run:")
        print("python scripts/check_idiom_rag_long_text.py")
        return 1

    config = PipelineConfig(locale="ko_ja", mock=True, score_threshold=0.0)
    retriever = IdiomRetriever(config)

    chunks = retriever._chunk_query(long_text)
    chunk_vectors = retriever.backend.embed(chunks)
    results = retriever.search(chunks, chunk_vectors)

    print("=== RAW RETRIEVAL RESULTS ===")
    if not results:
        print("(no results)")
    for rank, row in enumerate(results, start=1):
        print(_format_row(rank, row))
        print("---")

    print("=== PROMPT RAG CONTEXT ===")
    prompt_context = IdiomRetriever.build_context(results)
    print(prompt_context if prompt_context else "(empty)")

    build_debug_context = getattr(IdiomRetriever, "build_debug_context", None)
    if callable(build_debug_context):
        print("=== DEBUG CONTEXT ===")
        debug_context = build_debug_context(results)
        print(debug_context if debug_context else "(empty)")

    print("=== RUN COMMAND ===")
    print("python scripts/check_idiom_rag_long_text.py")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
